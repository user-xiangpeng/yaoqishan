package cn.javaex.yaoqishan.view;

/**
 * 标签表
 * 
 * @author 陈霓清
 */
public class TagInfo {
	private String id;		// 主键
	private String name;	// 标签名称
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
